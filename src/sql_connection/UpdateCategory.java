    private void updateBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_updateBtnActionPerformed
        // TODO add your handling code here:
        try {
            contr.setConnection();
            contr.createStmt();
            contr.getStmt().execute(String.format("UPDATE platillos SET R_Categoria = (SELECT ID_Categoria FROM categorias WHERE categoria = '%s') WHERE platillo = '%s'", new_category.getSelectedItem().toString(), meal_cb.getSelectedItem().toString()));
            JOptionPane.showMessageDialog(null, "Se ha modificado la categoria del platillo " + meal_cb.getSelectedItem().toString(), "Modificacion exitosa", JOptionPane.INFORMATION_MESSAGE);
        } catch (SQLException e) {
            Logger.getLogger(SQL_Connection.class.getName()).log(Level.SEVERE, null, e);
            JOptionPane.showMessageDialog(null, "Sucedio un error al modificar la categoria del platillo", "Error", JOptionPane.ERROR_MESSAGE);
        }
        finally {
            contr.closeStmt();
            contr.disconnect();
        }
    }//GEN-LAST:event_updateBtnActionPerformed
    
    public void initCatComboBox() {
        try {
            contr.setConnection();
            contr.createStmt();
            if (contr.runQuery("SELECT categoria FROM categorias")) {
                if (contr.getRS().next()) {
                    do {
                        new_category.addItem(contr.getString(1));
                    } while (contr.getRS().next());
                } else {
                    JOptionPane.showMessageDialog(null, "No hay categorias disponibles", "No hay categorias", JOptionPane.INFORMATION_MESSAGE);
                }
                contr.closeRS();
            }
            contr.closeStmt();
            contr.disconnect();
        } catch (SQLException e) {
            Logger.getLogger(SQL_Connection.class.getName()).log(Level.SEVERE, null, e);
            JOptionPane.showMessageDialog(null, "Sucedio un error al recuperar las categorias disponibles", "Error", JOptionPane.ERROR_MESSAGE);
        }
    }
    
    public void initMealComboBox() {
        try {
            contr.setConnection();
            contr.createStmt();
            if (contr.runQuery("SELECT platillos.platillo, categorias.categoria FROM platillos INNER JOIN categorias ON platillos.r_categoria = categorias.id_categoria")) {
                if (contr.getRS().next()) {
                    do {
                        meal_cb.addItem(contr.getString(1));
                        arr.add(contr.getString(2));
                    } while (contr.getRS().next());
                } else {
                    JOptionPane.showMessageDialog(null, "No hay platillos disponibles", "No hay platillos", JOptionPane.INFORMATION_MESSAGE);
                }
                contr.closeRS();
            }
            contr.closeStmt();
            contr.disconnect();
        } catch (SQLException e) {
            Logger.getLogger(SQL_Connection.class.getName()).log(Level.SEVERE, null, e);
            JOptionPane.showMessageDialog(null, "Sucedio un error al recuperar los platillos disponibles", "Error", JOptionPane.ERROR_MESSAGE);
        }
    }

