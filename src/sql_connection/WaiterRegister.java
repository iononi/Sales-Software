    //Separa el String pasado como parametro en los espacios, los mete en un array de String
    //y luego itera por el array para extrar la primera letra de cada nombre
    @Override
    public String getID(String name) {
        
        String parts[], waiterID = "";
        
        parts = name.split(" ");
        
        for (String part: parts)
            waiterID += part.charAt(0);
        
        return waiterID;
    }

