    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        String turno = "";
        String wID = "";
        try {
            ctr.setConnection();
            ctr.createStmt();
            turno = (turnoCB.getSelectedItem().equals("Matutino") ? "M" : "V");
            
            wID += getID(waiterName.getText());
            wID += getID(lastName1.getText());
            wID += getID(lastName2.getText());
            
            waiterID.setText(wID.toUpperCase());
            
            ctr.getStmt().execute(String.format("INSERT INTO meseros(ID_Mesero, Nombre_Mesero, ApellidoP, ApellidoM, Turno) VALUES (UPPER('%s'), '%s', '%s', '%s', '%s')", wID, waiterName.getText(), 
                    lastName1.getText(), lastName2.getText(), turno));
            JOptionPane.showMessageDialog(null, "Se ha registrado el nuevo mesero(a).", "Registro exitoso", JOptionPane.INFORMATION_MESSAGE);
        } catch (SQLException ex) {
            Logger.getLogger(SQL_Connection.class.getName()).log(Level.SEVERE, null, ex);
            JOptionPane.showMessageDialog(null, "ERROR: No se pudo registrar al mesero(a).", "Error de registro", JOptionPane.ERROR_MESSAGE);
        }
      
    }//GEN-LAST:event_jButton1ActionPerformed

